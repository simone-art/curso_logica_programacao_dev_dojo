package academy.devdojo.lacosderepeticao.fors;
/*
Fazer código que apresente de uma lista de números que começa no 15 até 200, quais números são quadrados
Números quadrados: números que ao ser multiplicados por ele mesmo geram um número positivo
 */
public class Aula02Exercicio01 {
    public static void main(String[] args) {
        for (int i = 15; i < 200; i++) {
            System.out.println(i * i);
        }
    }
}
