public class CondicionaisAula1 {
    /*
    Sintasis para condicionais : if(){}/
    dentro do paréntesis do if vai a condicional, que deve ser true ou false;
    dentro do ife vão estar os blocod de instrução que serão executados
    Se o if for executado, o else não e viceversa
     */
    public static void main(String[] args) {
        boolean condicao = true;
       if(condicao){
           System.out.println("Dentro do if");
       }else{
           System.out.println("Fora do If");
       }
    }
}
